package Bar;

public class Consumo {
	protected String nome;
	protected int num;
	protected int quant;
	protected double val;
	
	public void setNome(String n) {
		this.nome = n;
	}
	
	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public int getQuant() {
		return quant;
	}

	public void setQuant(int quant) {
		this.quant = quant;
	}

	public double getVal() {
		return val;
	}

	public void setVal(double val) {
		this.val = val;
	}

	

	public Consumo() {
		
	}
	
}
